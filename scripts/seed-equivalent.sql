-- ========== SCHEMA (CREATE TABLE so script works without JPA having run first) ==========
CREATE TABLE IF NOT EXISTS artists (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS directors (
  artist_id BIGINT PRIMARY KEY,
  CONSTRAINT fk_directors_artist FOREIGN KEY (artist_id) REFERENCES artists (id)
);

CREATE TABLE IF NOT EXISTS actors (
  artist_id BIGINT PRIMARY KEY,
  CONSTRAINT fk_actors_artist FOREIGN KEY (artist_id) REFERENCES artists (id)
);

CREATE TABLE IF NOT EXISTS users (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(255),
  email VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS movies (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(255),
  release_year INT,
  language VARCHAR(255),
  director_id BIGINT NOT NULL,
  CONSTRAINT fk_movies_director FOREIGN KEY (director_id) REFERENCES directors (artist_id)
);

CREATE TABLE IF NOT EXISTS movie_actors (
  movie_id BIGINT NOT NULL,
  actor_id BIGINT NOT NULL,
  PRIMARY KEY (movie_id, actor_id),
  CONSTRAINT fk_movie_actors_movie FOREIGN KEY (movie_id) REFERENCES movies (id) ON DELETE CASCADE,
  CONSTRAINT fk_movie_actors_actor FOREIGN KEY (actor_id) REFERENCES actors (artist_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS ratings (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  score INT,
  user_id BIGINT NOT NULL,
  movie_id BIGINT NOT NULL,
  CONSTRAINT uq_rating_user_movie UNIQUE (user_id, movie_id),
  CONSTRAINT fk_ratings_user FOREIGN KEY (user_id) REFERENCES users (id),
  CONSTRAINT fk_ratings_movie FOREIGN KEY (movie_id) REFERENCES movies (id)
);

